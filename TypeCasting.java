package myproject;

public class TypeCasting {
	public static void main(String[] args) {
		//type casting
		int a = 10;
		float b = a;
		System.out.println(a);
		System.out.println(b);
		//narrowing casting
		double c = 10.00000d;
		int d = (int)c;
		System.out.println("This is c value : "+c);
		System.out.println("This is d value : "+d);
	}

}
