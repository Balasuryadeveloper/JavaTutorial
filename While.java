package myproject;

public class While {
	public static void main(String[] args) {
		//While Loop
		//		Syntax
		//		while (condition) {
		//		  // code block to be executed
		//		}
		
		int i = 1;
		while(i < 10) {
			System.out.println(i);
			i++;
		}
		//do while
		//Syntax
		//		do {
		//			  // code block to be executed
		//			}
		//			while (condition);
		
		int j = 1;
		do {
			System.out.println(j);
			j++;
		}while(j < 6);
	}
}
