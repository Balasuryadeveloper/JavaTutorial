package myproject;

public class Boolean {
	public static void main(String[] args) {
		//Boolean
		boolean myBool = true;
		boolean myBool1 = false;
		System.out.println(myBool);
		System.out.println(myBool1);
		System.out.println(10 > 12); // false
		System.out.println(10 >= 12); // false
		System.out.println(10 < 12); // true
		System.out.println(10 <= 12); // true
		System.out.println(10 != 12); // true
	}
}
