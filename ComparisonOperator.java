package myproject;

public class ComparisonOperator {
	public static void main(String[] args) {
		//Comparison operator
		int a = 10;
		int b = 12;
		System.out.println(a == b);
		System.out.println(a != b);
		System.out.println(a > b);
		System.out.println(a >= b);
		System.out.println(a < b);
		System.out.println(a <= b);
	}
}
