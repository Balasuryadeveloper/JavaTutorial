package myproject;

public class AssignmentOperator {
	public static void main(String[] arg) {
		//Assignment operators
		int a = 10;
		System.out.println("A value is "+a);
		a+=2; //a=a+2
		System.out.println("A value is "+a);
		a-=1; //a=a-1
		System.out.println("A value is "+a);
		a*=2; //a=a*2
		System.out.println("A value is "+a);
		a/=2; //a=a/2
		System.out.println("A value is "+a);
		a%=2; //a=a%2
		System.out.println("A value is "+a);
		a&=10; //a=a&10
		System.out.println("A value is "+a);
		a|=3; //a=a|3
		System.out.println("A value is "+a);
		a^=3; //a=a^3
		System.out.println("A value is "+a);
		a>>=2; // a=a>>2
		System.out.println("A value is "+a);
		a<<=2; // a=a<<2
		System.out.println("A value is "+a);
	}
}
