package myproject;

public class ArithmeticOperator {
	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		int c = a+b;
		System.out.println("Sum of C is "+c);
		c = a-b;
		System.out.println("a-b is :"+c);
		c = a*b;
		System.out.println("A*b is "+c);
		c = a/b;
		System.out.println("a/b is "+c);
		c = a%b;
		System.out.println("a%b is "+c);
		a++;
		System.out.println("a++ is "+a);
		b--;
		System.out.println("b-- is "+b);
	}
}
